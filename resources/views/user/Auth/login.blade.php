<script>
    // 1. Inisialisasi pengecekan sesi aktif dari Auth Module
    Auth.redirectIfLoggedIn();

    const loginForm = document.getElementById('userLoginForm');
    const loginAlert = document.getElementById('loginAlert');
    const loginAlertMessage = document.getElementById('loginAlertMessage');
    const loginSubmit = document.getElementById('loginSubmit');
    const togglePassword = document.getElementById('togglePassword');
    const passwordInput = document.getElementById('password');
    const eyeIcon = document.getElementById('eyeIcon');
    const eyeSlashIcon = document.getElementById('eyeSlashIcon');

    // 2. Event Listener: Toggle Password Visibility
    togglePassword.addEventListener('click', () => {
        const type = passwordInput.getAttribute('type') === 'password' ? 'text' : 'password';
        passwordInput.setAttribute('type', type);
        eyeIcon.classList.toggle('hidden');
        eyeSlashIcon.classList.toggle('hidden');
    });

    // 3. Event Listener: Form Submit
    loginForm.addEventListener('submit', async function (event) {
        event.preventDefault();

        loginAlert.classList.add('hidden');
        Auth.setLoadingState(loginSubmit, true);

        const email = document.getElementById('email').value.trim();
        const password = document.getElementById('password').value.trim();

        try {
            // Memanggil API via service class modular JavaScript
            const payload = await Auth.login(email, password);
            const data = payload.data ?? payload;

            Auth.setSession(data.token, data.user);
            Auth.redirectBasedOnRole(data.user);

        } catch (error) {
            loginAlertMessage.textContent = error.message;
            loginAlert.classList.remove('hidden');
            Auth.setLoadingState(loginSubmit, false, `
                <span class="absolute left-0 inset-y-0 flex items-center pl-3">
                    <svg class="h-5 w-5 text-indigo-500 group-hover:text-indigo-400 transition-colors" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M11 16l-4-4m0 0l4-4m-4 4h14m-5 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h7a3 3 0 013 3v1" />
                    </svg>
                </span>
                <span>Masuk</span>
            `);
        }
    });
</script>
